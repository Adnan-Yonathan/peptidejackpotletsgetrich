export function getDoseReferencesForPeptide(peptideId: string) {
  return DOSING_REFERENCE_VARIANTS.filter((reference) => reference.peptideId === peptideId);
}

export function getDoseReferenceCoverage() {
  const references = new Set(DOSING_REFERENCE_VARIANTS.map((reference) => reference.peptideId));
  const missing = getPublishedPeptides()
    .filter((peptide) => !references.has(peptide.id))
    .map((peptide) => peptide.id);

  return {
    totalPeptides: getPublishedPeptides().length,
    coveredPeptides: references.size,
    missing,
  };
}

export function formatDoseReferenceStatus(status: DoseReferenceStatus) {
  if (status === "official_label") return "Official label";
  if (status === "research_reference") return "Research reference";
  if (status === "draft_needs_verification") return "Draft - verify source";
  return "Insufficient data";
}

export function formatDoseLane(lane: DoseReferenceLane) {
  if (lane === "low_reference_lane") return "Low/reference-start lane";
  if (lane === "upper_reference_lane") return "Upper-reference discussion lane";
  return "Standard reference lane";
}
