export function getVendorListingsForPeptide(peptideId: string): ResolvedVendorListing[] {
  return VENDOR_LISTINGS.filter((listing) => listing.peptideId === peptideId).map(resolveListing);
}

export function getVendorListingsForVendor(vendorId: string): ResolvedVendorListing[] {
  return VENDOR_LISTINGS.filter((listing) => listing.vendorId === vendorId).map(resolveListing);
}

export function getVendorListingForPeptideAndVendor(peptideId: string, vendorId: string): ResolvedVendorListing | undefined {
  const listing = VENDOR_LISTINGS.find((item) => item.peptideId === peptideId && item.vendorId === vendorId);
  return listing ? resolveListing(listing) : undefined;
}

export function getListingCurrencyCode(listing: Pick<VendorListingData, "vendorId" | "currencyCode">): CurrencyCode {
  if (listing.currencyCode) return listing.currencyCode;
  return listing.vendorId === "xl-peptides" ? "EUR" : "USD";
}

export function getListingShopperRegion(
  listing: Pick<VendorListingData, "vendorId" | "shopperRegion">
): ShopperRegion {
  if (listing.shopperRegion) return listing.shopperRegion;
  return listing.vendorId === "amino-club" ? "us" : "international";
}

function normalizeListingMoney(
  listing: Pick<VendorListingData, "vendorId" | "currencyCode">,
  value?: number
) {
  if (value === undefined) return undefined;
  if (listing.vendorId === "xl-peptides" && getListingCurrencyCode(listing) === "EUR") {
    return roundMoney(value * GBP_TO_EUR_RATE);
  }
  return value;
}

export function getListingPriceBounds(
  listing: Pick<VendorListingData, "vendorId" | "currencyCode" | "listedPriceLowUsd" | "listedPriceHighUsd">
) {
  const low = normalizeListingMoney(listing, listing.listedPriceLowUsd);
  const high = normalizeListingMoney(listing, listing.listedPriceHighUsd);
  if (low === undefined || high === undefined) return undefined;
  return { low, high, currencyCode: getListingCurrencyCode(listing) };
}

export function getListingPriceLabel(
  listing: Pick<
    VendorListingData,
    "vendorId" | "currencyCode" | "listedPriceLowUsd" | "listedPriceHighUsd" | "typicalRetailPriceRangeUsd"
  >
) {
  const bounds = getListingPriceBounds(listing);
  if (!bounds) {
    return listing.vendorId === "xl-peptides"
      ? listing.typicalRetailPriceRangeUsd.replaceAll("£", "€")
      : listing.typicalRetailPriceRangeUsd;
  }

  const symbol = bounds.currencyCode === "EUR" ? "€" : "$";
  if (Math.abs(bounds.low - bounds.high) < 0.01) {
    return `${symbol}${bounds.low.toFixed(2)} shown`;
  }

  return `${symbol}${bounds.low.toFixed(2)}-${symbol}${bounds.high.toFixed(2)} shown`;
}

function resolveListing(listing: VendorListingData): ResolvedVendorListing {
  return {
    ...listing,
    typicalRetailPriceRangeUsd: getListingPriceLabel(listing),
    peptide: getPeptideById(listing.peptideId),
    vendor: getVendorById(listing.vendorId),
  };
}

